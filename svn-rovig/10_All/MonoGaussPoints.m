function xw = MonoGaussPoints(n)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Function 1DGaussPoints provides the Gaussian points and weights %
% for the Gaussian quadrature of order n for the standard lines. % %%
% Input: n - the order of the Gaussian quadrature (n<=4) % %%
 %
%
%
% %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

if (n == 1)
xw=[-0.5773502692 1.00000000000000 
    +0.5773502692 1.00000000000000];
elseif (n == 2)
xw=[-0.7745966692 0.5555555556 
     0.0000000000 0.8888888889 
    +0.7745966692 0.5555555556];
elseif (n == 3)
xw=[-0.8611363116 0.3478548451
    -0.3399810436 0.6521451549
    +0.3399810436 0.6521451549
    +0.8611363116 0.3478548451];
elseif (n == 4)
xw=[-0.9061798459 0.2369268851 
    -0.5384693101 0.4786286705   
     0.0000000000 0.5688888889 
    +0.5384693101 0.4786286705    
    +0.9061798459 0.2369268851];
else
error('Bad input n');
end

end